interface CountrySelectProps {
  value?: string;
  onValueChange?: (value: string) => void;
  placeholder?: string;
  className?: string;
  error?: boolean;
}

export function CountrySelect({
  value,
  onValueChange,
  placeholder = "Sélectionner un pays",
  className,
  error,
}: CountrySelectProps) {
  const selectedCountry = COUNTRIES.find((country) => country.name === value);

  return (
    <Select value={value} onValueChange={onValueChange}>
      <SelectTrigger
        className={cn(
          "border-yellow-500/30 focus:border-yellow-500",
          error && "border-red-500 focus:border-red-500",
          className
        )}
      >
        <div className="flex items-center gap-2 flex-1 min-w-0">
          {selectedCountry && (
            <img
              src={`https://flagcdn.com/w20/${selectedCountry.code}.png`}
              alt=""
              className="h-4 w-5 object-cover rounded-sm flex-shrink-0"
              loading="lazy"
              onError={(e) => {
                // Fallback si le drapeau ne charge pas
                e.currentTarget.style.display = "none";
              }}
            />
          )}
          <SelectValue placeholder={placeholder} />
        </div>
      </SelectTrigger>
      <SelectContent className="max-h-[300px] overflow-y-auto">
        {COUNTRIES.map((country) => (
          <SelectItem key={country.code} value={country.name} textValue={country.name}>
            <img
              src={`https://flagcdn.com/w20/${country.code}.png`}
              alt=""
              className="h-4 w-5 object-cover rounded-sm flex-shrink-0"
              loading="lazy"
              onError={(e) => {
                // Fallback si le drapeau ne charge pas
                e.currentTarget.style.display = "none";
              }}
            />
          </SelectItem>
        ))}
      </SelectContent>
    </Select>
  );
}